from io import BytesIO
from datetime import datetime

from reportlab.lib.pagesizes import A4
from reportlab.pdfgen import canvas
from reportlab.lib.units import cm


def generate_patient_report_pdf(
    patient_data: dict,
    probability: float,
    label: str,
    shap_top_drivers: list[tuple[str, float]] | None = None,
) -> bytes:
    """
    Returns PDF bytes for Streamlit download.
    shap_top_drivers example: [("thalach", +0.12), ("oldpeak", +0.09), ...]
    """
    buffer = BytesIO()
    c = canvas.Canvas(buffer, pagesize=A4)
    width, height = A4

    # Layout constants
    x_left = 2.0 * cm
    y = height - 2.0 * cm
    line_h = 0.65 * cm

    # Title
    c.setFont("Helvetica-Bold", 16)
    c.drawString(x_left, y, "Heart Disease Risk Prediction Report")
    y -= 1.2 * cm

    # Meta
    c.setFont("Helvetica", 10)
    c.drawString(x_left, y, f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    y -= 1.0 * cm

    # Summary box
    c.setFont("Helvetica-Bold", 12)
    c.drawString(x_left, y, "Prediction Summary")
    y -= 0.7 * cm

    c.setFont("Helvetica", 11)
    c.drawString(x_left, y, f"Risk Probability: {probability:.2f}")
    y -= line_h
    c.drawString(x_left, y, f"Risk Label: {label}")
    y -= 1.0 * cm

    # Inputs
    c.setFont("Helvetica-Bold", 12)
    c.drawString(x_left, y, "Patient Inputs")
    y -= 0.7 * cm

    c.setFont("Helvetica", 10)

    # Print inputs in two columns
    items = list(patient_data.items())
    col_gap = 9.0 * cm
    col1_x = x_left
    col2_x = x_left + col_gap

    for i, (k, v) in enumerate(items):
        col_x = col1_x if i % 2 == 0 else col2_x
        if i % 2 == 0 and i != 0:
            y -= line_h
        c.drawString(col_x, y, f"{k}: {v}")

        # Page break protection
        if y < 3.0 * cm:
            c.showPage()
            y = height - 2.0 * cm
            c.setFont("Helvetica", 10)

    y -= 1.2 * cm

    # SHAP drivers (optional)
    if shap_top_drivers:
        c.setFont("Helvetica-Bold", 12)
        c.drawString(x_left, y, "Top Risk Drivers (Explainability)")
        y -= 0.7 * cm

        c.setFont("Helvetica", 10)
        for feat, val in shap_top_drivers[:5]:
            sign = "+" if val >= 0 else "-"
            c.drawString(x_left, y, f"- {feat}: {sign}{abs(val):.4f}")
            y -= 0.55 * cm

            if y < 3.0 * cm:
                c.showPage()
                y = height - 2.0 * cm
                c.setFont("Helvetica", 10)

        y -= 0.5 * cm

    # Disclaimer
    c.setFont("Helvetica-Bold", 10)
    c.drawString(x_left, y, "Disclaimer")
    y -= 0.55 * cm

    c.setFont("Helvetica", 9)
    disclaimer = (
        "This report is generated by a machine learning decision-support system. "
        "It is NOT a medical diagnosis. Consult a qualified healthcare professional "
        "for clinical interpretation and treatment decisions."
    )
    # simple wrap
    max_chars = 95
    for i in range(0, len(disclaimer), max_chars):
        c.drawString(x_left, y, disclaimer[i:i + max_chars])
        y -= 0.5 * cm

    # Footer
    c.setFont("Helvetica-Oblique", 8)
    c.drawString(x_left, 1.5 * cm, "Heart Disease Risk Prediction Platform - Academic Project | AQL_DARK_WashiTec")

    c.showPage()
    c.save()
    buffer.seek(0)
    return buffer.read()